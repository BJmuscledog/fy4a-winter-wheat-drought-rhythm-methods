def get_center_angel(angel, rows_start, rows_end, cols_start, cols_end):
    angel_part = angel[rows_start:rows_end, cols_start:cols_end]
    center_index = (angel_part.shape[0] // 2, angel_part.shape[1] // 2)
    center_value = angel_part[center_index]
    if np.isnan(center_value):
        valid_angles = angel_part[~np.isnan(angel_part)]
        if len(valid_angles) > 0:
            c_angle = np.nanmax(valid_angles)  # 或 np.nanmean(valid_angles) for 平均
        else:
            c_angle = 58.0  # 默认太阳天顶角 (典型值, 避免全跳过)
            print(f"[WARN] All NaN in partition rows {rows_start}-{rows_end}, used default {c_angle}")
    else:
        c_angle = center_value
    return c_angle

