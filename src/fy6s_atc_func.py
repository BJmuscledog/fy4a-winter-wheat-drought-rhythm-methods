def fy6s_atc(fy_dir, angel_dir, out_dir,
             bandids, rows_parts, cols_parts,
             year, month, day, start_n, end_n,
             f_lulo=110.35571015, f_lula=42.6154530898):
    """基于 6S 模型对风云四号影像做大气校正"""

    fy_list = list(glob.iglob(os.path.join(fy_dir, '*.tif')))
    angle_list = list(glob.iglob(os.path.join(angel_dir, '*.tif')))

    fy_ro, fy_co = rows_parts[-1], cols_parts[-1]  # 去掉 -1，保证完整输出

    for n in range(start_n, end_n):
        start = time.time()
        print("Start time:", time.strftime("%Y-%m-%d %H:%M:%S", time.localtime()))

        # 角度矩阵
        saaz = readtiff(angle_list[n * 5 + 0], 1)
        saze = readtiff(angle_list[n * 5 + 1], 1)
        suaz = readtiff(angle_list[n * 5 + 2], 1)
        suze = readtiff(angle_list[n * 5 + 4], 1)

        out_band = [np.full((fy_ro, fy_co), np.nan) for _ in range(len(bandids))]

        for id in range(len(bandids)):
            fy_band = readtiff(fy_list[n], bandids[id])

            # ✅ 正确的 NaN 判断
            mask = (fy_band > 0) & (~np.isnan(suze))
            toas = np.where(mask, fy_band / (np.cos(np.radians(suze))), np.nan)

            for i in range(len(rows_parts) - 1):
                for j in range(len(cols_parts) - 1):
                    rows_start, rows_end = rows_parts[i], rows_parts[i + 1]
                    cols_start, cols_end = cols_parts[j], cols_parts[j + 1]
                    p_rows, p_cols = rows_end - rows_start, cols_end - cols_start

                    # 中心角度
                    c_saaz = get_center_angel(saaz, rows_start, rows_end, cols_start, cols_end)
                    c_saze = get_center_angel(saze, rows_start, rows_end, cols_start, cols_end)
                    c_suaz = get_center_angel(suaz, rows_start, rows_end, cols_start, cols_end)
                    c_suze = get_center_angel(suze, rows_start, rows_end, cols_start, cols_end)

                    if not np.isnan(c_saaz) and not np.isnan(c_saze) \
                       and not np.isnan(c_suaz) and not np.isnan(c_suze):

                        c_toa = toas[rows_start:rows_end, cols_start:cols_end]

                        try:
                            (temp_xa, temp_xb, temp_xc) = ac_6s(
                                c_saaz, c_saze, c_suaz, c_suze,
                                bandids[id], year, month, day,
                                rows_start, cols_start, p_rows, p_cols,
                                f_lulo, f_lula
                            )

                            # ✅ 改正 NaN 判断
                            valid_mask = ~np.isnan(c_toa)
                            y = np.where(valid_mask, temp_xa * c_toa - temp_xb, np.nan)
                            atc_band = np.where(~np.isnan(y), y / (1 + y * temp_xc), np.nan)

                            out_band[id][rows_start:rows_end, cols_start:cols_end] = atc_band

                        except Exception as e:
                            print(f'Error for saa {c_saaz} at row {rows_start}', e)

        tif_name = os.path.basename(fy_list[n]).split('CLR_')[1]
        out_filename = os.path.join(out_dir, '6s_' + tif_name)
        write_multiplebands_tiff(out_filename, out_band, fy_co, fy_ro)

        print("Time:", (time.time() - start) / 60, "Minutes")



